const BOOKLISTS = {
  business: BUSINESS_TITLES,
  humanities: HUMANITIES_TITLES,
};

export const CATEGORY_NAME_BY_ID = Object.fromEntries(
  VISIBLE_CATEGORIES.map((category) => [category.id, category.name]),
);

export const CATEGORY_NAME_EN_BY_ID = Object.fromEntries(
  VISIBLE_CATEGORIES.map((category) => [category.id, category.nameEn]),
);

export function getProductsByCategory(categoryId) {
  return (BOOKLISTS[categoryId] ?? []).map((title, index) => {
    const type = TYPE_PATTERN[index % TYPE_PATTERN.length];
    return {
      id: `${categoryId}-${index + 1}`,
      title,
      titleEn: title,
      type,
      price: 1,
      duration: 10,
    };
  });
}

export function getFeaturedProducts(limit = 4) {
  return [
    ...getProductsByCategory('business').slice(0, 2),
    ...getProductsByCategory('humanities').slice(0, 2),
  ].slice(0, limit);
}
